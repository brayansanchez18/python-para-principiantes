# tipo de dato String
x = "Hola Mundo"
print(x)
print(type(x))

# tipo de dato int
x = 7
print(x) 
print(type(x))

y = -7
print(y) 
print(type(y))

z = 12345678901234567890 
print(z) 
print(type(z))

# tipo de dato float
x = 77.5
print(x)
print(type(x))

# tipo de dato bool
# al ser palabras reservadas deben iniciar con mayuscula
x = True
print(x)
print(type(x))

y = False
print(y)
print(type(y))